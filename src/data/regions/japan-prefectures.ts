// Helper function to get prefecture by code
export function getPrefectureByCode(code: string): PrefectureInfo | undefined {
  return japanesePrefectures.find(p => p.code === code);
}

// Helper function to get prefecture by name
export function getPrefectureByName(name: string): PrefectureInfo | undefined {
  const lower = name.toLowerCase();
  return japanesePrefectures.find(
    p => p.name.toLowerCase() === lower || 
         p.nameJa === name ||
         p.capital.toLowerCase() === lower ||
         p.capitalJa === name
  );
}

// Helper function to get prefectures by region
export function getPrefecturesByRegion(region: string): PrefectureInfo[] {
  return japanesePrefectures.filter(p => p.region === region);
}
